#!/usr/bin/perl
use strict;
use warnings;

# Задание 3: Конструирование третьего массива из двух

print "Введите элементы первого массива (по одному на строку, Ctrl+D для завершения):\n";
chomp(my @arr1 = <>);

print "Введите элементы второго массива (по одному на строку, Ctrl+D для завершения):\n";
chomp(my @arr2 = <>);

# Конструирование третьего массива
my @result;
my $pos = 0;
my $i1 = 0;
my $i2 = 0;

while (1) {
    if ($pos % 2 == 0) {  # Четный индекс: из первого массива
        last if $i1 >= @arr1;
        $result[$pos] = $arr1[$i1++];
    } else {  # Нечетный индекс: из второго массива
        last if $i2 >= @arr2;
        $result[$pos] = $arr2[$i2++];
    }
    $pos++;
}

# Вывод результата
print "\nТретий массив: @result\n";